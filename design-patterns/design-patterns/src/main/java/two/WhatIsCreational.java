package two;

public class WhatIsCreational {

    /**
     *
     * Creational patterns bring us a solution in order to create an object.
     * Create an object seems very easy task in the beginning,
     * but it can create a complexity.
     *
     * In creational  pattern there are two main ideas :
     *  - Encapsulating knowledge about which concrete classes the system uses.
     *  - Hiding how instances of these concrete classes are created and combined.
     *
     *  If we categorized creational design patterns :
     *    - Object-creational patterns - deal with Object creation
     *    - Class-creational patterns  - deal with Class-instantiation
     *
     *
     *  - Abstract Factory Pattern
     *  - Builder Pattern
     *  - Factory Pattern
     *  - Prototype Pattern
     *  - Singleton Pattern
     */
}
