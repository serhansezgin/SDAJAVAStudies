package thirteen.open.closed.principle;

public class OpenClosedPrinciple {
    /**
     *
     * The open closed principle
     * software entities (classes, modules, functions, etc.) should be open for extension,
     * but closed for modification.
     *
     * What is extension ?
     * What is modification?
     *
     * One can extend the implementation (of a class or method) of logic and/or functionality.
     * One should not modify the implementation (of a class or method) of logic and/or functionality.
     * If there is a bug and you want to fix it, we don't call this case as a modification!
     *
     */
}
